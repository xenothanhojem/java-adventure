export function getTheoryUnit(unitId) {
  return THEORY_UNITS.find((u) => u.unitId === unitId) || null;
}

export function getAllTheoryQuestions(unitId) {
  const unit = getTheoryUnit(unitId);
  if (!unit) return [];
  return unit.topics.flatMap((t) => t.questions || []);
}
